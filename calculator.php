            if ($data && isset($data['info']) && $data['info'] == 'Central Bank of Myanmar') {
                $rates =  $data['rates'];

                if ($_SERVER['REQUEST_METHOD'] === 'POST') {
                    $amount = $_POST['amount'];
                    // $fromCurrency = $_POST['from_currency'];
                    $toCurrency = $_POST['to_currency'];

                    // $fromRate = $rates[$fromCurrency];
                    $toRate = $rates[$toCurrency];
                    $integerNumber = (int)str_replace(',', '', $toRate);
                    $intValue = (int)  $integerNumber;
                    $convertedAmount = $amount * $intValue;
                    $Amount = $amount / $intValue;

                    
                    $fileName = "saveRecord.txt";
                    if (!file_exists($fileName)) {
                        touch($fileName);
                    }
                    $fileStream = fopen($fileName, "a");
                    fwrite($fileStream, "\n$amount * $intValue = $convertedAmount MMK");
                    fwrite($fileStream, "\n$amount / $intValue =   $Amount Currency");

                    fclose($fileStream);



                    echo "<p>  Currency to MMK---> {$convertedAmount } MMK</p>";
                    echo "<p> MMK to Currency ---> {$Amount} Currency </p>";
                }
